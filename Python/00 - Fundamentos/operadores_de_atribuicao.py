saldo = 500
saldo += 100 #saldo = saldo + 100
print(saldo)

saldo -= 100 #saldo = saldo - 100
print(saldo)

saldo *= 5 #saldo = saldo * 5
print(saldo)

saldo /= 2 #saldo = saldo / 2
print(saldo)

saldo //= 2 #saldo = saldo // 2
print(saldo)

saldo %= 3 #saldo = saldo % 3
print(saldo)

saldo **= 2 #saldo = saldo ** 2
print(saldo)

