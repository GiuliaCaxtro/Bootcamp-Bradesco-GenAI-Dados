saldo = 2.000
saque = 400
# Condição ternária
resultado = 'Sucesso' if saldo >= saque else 'Falha'
print(f'{resultado} ao realizar o saque!')
#Ternario é uma forma mais compacta de escrever um if-else, onde a expressão é avaliada e retorna um valor com base na condição. No exemplo acima, se o saldo for maior ou igual ao saque, o resultado será 'SUCESSO', caso contrário, será 'FALHA'.